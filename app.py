# 4. 동적 점수 계산기 함수 구현 (수학/정보 알고리즘 요건 충족)
def calculate_scores(selected_major_name, major_info):
    scores = []
    for _, row in df_curriculum.iterrows():
        sub_name = row["과목명"]
        sub_domain = row["영역"]
        
        # 기본 점수 메커니즘
        if sub_name in major_info["핵심"]:
            score = 98
        elif sub_name in major_info["권장"]:
            score = 85
        else:
            score = 60
            # 계열별 도메인 가산점 규칙 적용
            if any(selected_major_name in major_db[k] for k in ["공학계열", "자연과학계열", "의학보건계열"]) and sub_domain in ["과학", "수학", "정보"]:
                score += 10
            elif any(selected_major_name in major_db[k] for k in ["인문사회계열"]) and sub_domain in ["사회", "영어"]:
                score += 10
        scores.append(score)
    return scores

